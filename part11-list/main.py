# List

data_angka = [1, 2, 3, 4, 5]
print(data_angka)

data_angka.append(6)
print(data_angka)

data_baru = [7, 8, 9]
data_angka.extend(data_baru)
print(data_angka)

data_angka.insert(0, 0)
print(data_angka)

data_angka.remove(0)
print(data_angka)

data_angka.pop()
print(data_angka)

data_angka.reverse()
print(data_angka)

data_angka.sort()
print(data_angka)
